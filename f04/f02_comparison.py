def main():
    """
    연산자	설명	예제	결과
    ==	같음	5 == 3	FALSE
    !=	다름	5 != 3	TRUE
    >	큼	5 > 3	TRUE
    <	작음	5 < 3	FALSE
    >=	크거나 같음	5 >= 5	TRUE
    <=	작거나 같음	5 <= 3	FALSE
    """
    print(f'5==3 : {5==3}')
    print(f'5!=3 : {5 != 3}')
    print(f'5<=3 : {5 <= 3}')
    print(f'5>=3 : {5 >= 3}')

if __name__ == '__main__':
    main()
